#include <stdio.h>

extern const Group groups[NUM_GROUPS];
extern const BoundlessGroup boundlessGroups[NUM_GROUPS];

Group getGroup(const size_t index) {
	return groups[index];
}

BoundlessGroup* cudifyGroups(void) {
	BoundlessGroup* bg;
	cudaError_t err = cudaMalloc(&bg, NUM_GROUPS * sizeof(BoundlessGroup));
	if (err != cudaSuccess) return NULL;
	err = cudaMemcpy(bg, boundlessGroups, NUM_GROUPS * sizeof(BoundlessGroup), cudaMemcpyHostToDevice);
	if (err != cudaSuccess) {
		cudaFree(bg);
		return NULL;
	}
	return bg;
}

Byte* makeGroupsArray(size_t maximumRound) {
    size_t n_bits = NUM_GROUPS * maximumRound;
    size_t bytes = ((n_bits + 7) / 8);
    Byte* groups = (Byte *)malloc(bytes);
    if (!groups) {
        perror("makeGroupsArray could not allocate bits: ");
        return groups;
    }
    bool inbounds;
    double round_d;
    Group group;
    for (size_t g = 0; g < NUM_GROUPS; g++) {
        group = getGroup(g);
        for (size_t round = 0; round < maximumRound; round++) {
            round_d = (double)round;
            inbounds = false;
            for (int j = 0; j < NUM_BOUNDS; j++) {
                double lbound = group.bounds[2 * j];
                double ubound = group.bounds[1 + 2 * j];
                if ((lbound <= round_d) && (round_d <= ubound)) {
                    inbounds = true;
                    break;
                }
            }
            bitSet(groups, round * NUM_GROUPS + g, inbounds);
        }
    }
    return groups;
}

Byte* cu_makeGroupsArray(size_t maximumRound) {
    cudaError_t err = cudaSuccess;
    size_t n_bits = NUM_GROUPS * maximumRound;
    size_t bytes = ((n_bits + 7) / 8);
    Byte* cu_bits = NULL;
    Byte* groups = makeGroupsArray(maximumRound);
    if (!groups) goto fail;
    err = cudaMalloc(&cu_bits, bytes);
    if (err != cudaSuccess) goto fail;
    err = cudaMemcpy(cu_bits, groups, bytes, cudaMemcpyHostToDevice);
    if (err != cudaSuccess) goto fail;
    free(groups);
    return cu_bits;
fail:
    if (groups) free(groups);
    if (cu_bits) cudaFree(cu_bits);
    return NULL;
}


